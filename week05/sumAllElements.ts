// -  Create a variable named `numbers` with the following content:
//   `[3, 4, 5, 6, 7]`
// -  Log the sum of the elements of `numbers` to the console

let numbers5: number[]= [1, 2, 3, 4, 5];

let sum = 0; 
for (let i = 0; i < numbers5.length; i++){
     sum += numbers5[i]; } 
console.log(sum); 

/* We initialize a variable sum as 0 to store the result and use the for loop to visit each element and add them to the sum of the array. */

